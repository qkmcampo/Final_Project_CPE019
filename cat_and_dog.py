MODEL_PATH = "CAT_DOG_model.h5"
IMG_SIZE = (150, 150)
CLASS_NAMES = ['Cat', 'Dog']

@st.cache_resource
def load_local_model():
    model = load_model(MODEL_PATH)
    return model

model = load_local_model()

